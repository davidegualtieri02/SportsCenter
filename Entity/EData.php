<?php
class Data{
    private int $giorno ;
    private String $mese;
    private int $anno;



    public function __construct($giorno,$mese,$anno){
        $this->giorno = $giorno;
        $this->mese = $mese;
        $this->anno = $anno;
    }
    public function getGiorno(){
        return $this->giorno;
    }
    public function setGiorno($giorno){
        $this->giorno = $giorno;
    
    }
    public function getMese(){
        return $this->mese;
    }
    public function setMese($mese){
        $this->giorno = $mese;
    }
    public function getAnno(){
        return $this->anno;
    }
    public function setAnno($anno){
        $this->giorno = $anno;
    }



}